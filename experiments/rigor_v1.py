"""
Rigorous epiplexity experiment suite (ROADMAPv2 programmes A, B, D, E).

Outputs evidence/results/rigor_v1.json.
Run:  python -m experiments.rigor_v1  [--quick]

Four blocks:
  A/D  scaling   — every agent × N ∈ {100, 300, 1000, 3000} × 3 seeds
  B    orderings — 6 canonical orderings applied to det_greedy + det_combo at N=500
  E1   pareto    — (|P|, H_T) scatter from 3 reference corpora
  E2   matchups  — round-robin win rates + cross-agent H_T matrix

Corpora are persisted under evidence/corpora/ so re-analysis is cheap.
"""
from __future__ import annotations
import argparse, hashlib, json, math, random, time
from collections import Counter, defaultdict
from pathlib import Path
from statistics import mean, stdev

from hexgo import EisensteinGreedyAgent, RandomAgent
from hexgo.agents import ForkAwareAgent, PotentialGradientAgent, ComboAgent
from hexgo.epiplexity import (
    Corpus, Game, MDLResult, MarkovObserver, agent_cross_entropy, generate,
    gzip_bits, measure, program_length, tokens,
)
from hexgo.game import HexGame

ROOT = Path(__file__).resolve().parent.parent
RESULTS = ROOT / "evidence" / "results"
CORPORA = ROOT / "evidence" / "corpora"
RESULTS.mkdir(exist_ok=True)
CORPORA.mkdir(exist_ok=True)

# ── agent roster ─────────────────────────────────────────────────────────────

def make_factories():
    return {
        "random":     lambda: RandomAgent(),
        "greedy":     lambda: EisensteinGreedyAgent(),
        "fork_a2":    lambda: ForkAwareAgent(alpha=2.0, eps=0.0),
        "fork_a4":    lambda: ForkAwareAgent(alpha=4.0, eps=0.0),
        "potgrad":    lambda: PotentialGradientAgent(eps=0.0),
        "combo":      lambda: ComboAgent(),
    }

AGENT_CLASS = {
    "random":  RandomAgent,
    "greedy":  EisensteinGreedyAgent,
    "fork_a2": ForkAwareAgent,
    "fork_a4": ForkAwareAgent,
    "potgrad": PotentialGradientAgent,
    "combo":   ComboAgent,
}


# ── corpus caching ───────────────────────────────────────────────────────────

def corpus_path(name, n, seed):
    return CORPORA / f"{name}_N{n}_s{seed}.pkl.gz"

def build_corpus(name, factory, n, seed) -> Corpus:
    p = corpus_path(name, n, seed)
    if p.exists():
        return Corpus.load(p)
    c = generate(factory, factory, n=n, seed=seed)
    c.manifest["label"] = name
    c.save(p)
    return c


# ── six orderings (ROADMAP B.1) ──────────────────────────────────────────────
#
# Shannon entropy of the move *multiset* is invariant under all six;
# Markov cross-entropy is not.

def _token_stream_from_games(games_tokens: list[list]) -> list:
    """Flatten [(tokens_of_game_1), ...] into a single stream with None separators."""
    out = []
    for toks in games_tokens:
        out.extend(toks)
        out.append(None)
    return out

def order_temporal(game: Game) -> list[tuple[int, int]]:
    if not game.moves: return []
    q0, r0 = game.moves[0]
    return [(q - q0, r - r0) for q, r in game.moves]

def order_reverse(game: Game) -> list[tuple[int, int]]:
    return list(reversed(order_temporal(game)))

def order_radial_in(game: Game) -> list[tuple[int, int]]:
    # |q + ω r|² = q² + qr + r² in Eisenstein; ordering by q²+qr+r² is equivalent up to ties
    toks = order_temporal(game)
    return sorted(toks, key=lambda t: (t[0]**2 + t[0]*t[1] + t[1]**2, t))

def order_radial_out(game: Game) -> list[tuple[int, int]]:
    return list(reversed(order_radial_in(game)))

# Axis-stratified: partition by which of the 3 A₂-axes the move is "most aligned with"
# (by dot product with unit axes). AXES from game.py gives us the three directions.
from hexgo.game import AXES  # after patching sys.path via hexgo.game bridge
import math as _math

def _axis_bucket(q: int, r: int) -> int:
    # pick axis that maximises |q*dq + r*dr|
    best_i, best_v = 0, -1
    for i, (dq, dr) in enumerate(AXES):
        v = abs(q * dq + r * dr)
        if v > best_v:
            best_v, best_i = v, i
    return best_i

def order_axis(game: Game) -> list[tuple[int, int]]:
    toks = order_temporal(game)
    return sorted(toks, key=lambda t: (_axis_bucket(*t), t))

def order_d6_canonical(game: Game) -> list[tuple[int, int]]:
    """Map each move to its D₆ orbit representative (canonical form), then temporal."""
    toks = order_temporal(game)
    # D₆ acting on (q, r): generated by rotation by 60° and reflection
    # Rotation by 60°: (q, r) -> (q+r, -q). Reflection: (q, r) -> (r, q).
    def orbit(q, r):
        pts = set()
        cur = (q, r)
        for _ in range(6):
            pts.add(cur)
            pts.add((cur[1], cur[0]))
            cur = (cur[0] + cur[1], -cur[0])
        return min(pts)
    return [orbit(q, r) for q, r in toks]

ORDERINGS = {
    "temporal":       order_temporal,
    "reverse":        order_reverse,
    "radial_in":      order_radial_in,
    "radial_out":     order_radial_out,
    "axis":           order_axis,
    "d6_canonical":   order_d6_canonical,
}

def measure_ordering(corpus: Corpus, fn, order=3) -> dict:
    games_tokens = [fn(g) for g in corpus.games if g.moves]
    stream = _token_stream_from_games(games_tokens)
    split = int(len(stream) * 0.8)
    while split < len(stream) and stream[split] is not None:
        split += 1
    train, test = stream[:split], stream[split:]
    obs = MarkovObserver(order).fit(train)
    try:
        sub = test[:3000] if len(test) > 3000 else test
        if sub and sub[-1] is not None:
            sub = sub + [None]
        obs.fit_weights(sub, grid=3)
    except Exception:
        pass
    H = obs.cross_entropy(test)
    S = obs.program_length()
    # Shannon (unigram) for the multiset — should be invariant
    flat = [t for t in stream if t is not None]
    counts = Counter(flat)
    total = len(flat)
    H_shannon = -sum((c/total) * math.log2(c/total) for c in counts.values()) if total else 0.0
    return {"H_T": H, "S_T_bits": S, "H_shannon_bits": H_shannon, "n_tokens": total}


# ── experiment blocks ────────────────────────────────────────────────────────

def _measure_fast(corpus: Corpus, weights=None, order: int = 3,
                  fit_weights_here: bool = False) -> MDLResult:
    """Faster measure: optionally accept pre-computed interpolation weights,
    subsample held-out set to 3000 tokens for weight fitting."""
    toks = tokens(corpus)
    split = int(len(toks) * 0.8)
    while split < len(toks) and toks[split] is not None:
        split += 1
    train, test = toks[:split], toks[split:]
    obs = MarkovObserver(order).fit(train)
    if weights is not None:
        obs.weights = list(weights)
    elif fit_weights_here:
        # subsample test for weight tuning
        sub = test[:3000] if len(test) > 3000 else test
        # bump last token to separator so no dangling context
        if sub and sub[-1] is not None:
            sub = sub + [None]
        obs.fit_weights(sub, grid=3)  # grid=3: C(3+3,3)=20 simplex points
    H = obs.cross_entropy(test)
    S = obs.program_length()
    n = sum(1 for t in toks if t is not None)
    gz_H, gz_total = gzip_bits(corpus)
    return MDLResult(corpus.manifest.get("label", "?"),
                     len(corpus.games), n, H, S, gz_H, gz_total), obs.weights


def block_scaling(quick: bool) -> dict:
    """A/D: H_T and gzip across corpus sizes and seeds.

    Weights strategy: tune once per agent on the largest N at seed 0; reuse
    everywhere else. This is a defensible single-hyperparameter-per-agent choice.
    """
    factories = make_factories()
    Ns = [100, 300] if quick else [100, 300, 1000, 3000]
    seeds = [0, 1] if quick else [0, 1, 2]
    N_tune = max(Ns)
    out = {}
    tuned_weights = {}
    # pass 1: tune weights on (largest N, seed 0) corpus
    for name, fac in factories.items():
        t0 = time.time()
        c = build_corpus(name, fac, N_tune, 0)
        r, w = _measure_fast(c, fit_weights_here=True)
        tuned_weights[name] = w
        print(f"  tune-weights {name} N={N_tune}: w={[round(x,2) for x in w]} "
              f"H={r.markov_H:.3f} [{time.time()-t0:.1f}s]", flush=True)

    # pass 2: measure across all Ns/seeds using the tuned weights
    for name, fac in factories.items():
        rows = []
        for N in Ns:
            for seed in seeds:
                t0 = time.time()
                c = build_corpus(name, fac, N, seed)
                r, _ = _measure_fast(c, weights=tuned_weights[name])
                rows.append({
                    "N": N, "seed": seed,
                    "n_tokens": r.n_tokens,
                    "markov_H": r.markov_H,
                    "markov_S_bits": r.markov_S,
                    "gzip_bpt": r.gzip_H,
                    "gzip_total_bits": r.gzip_total,
                    "wall_s": round(time.time() - t0, 2),
                })
                print(f"  scaling {name} N={N} seed={seed}: H={r.markov_H:.3f} "
                      f"S={r.markov_S} gz={r.gzip_H:.3f} [{rows[-1]['wall_s']}s]", flush=True)
        out[name] = rows
    out["_tuned_weights"] = {k: list(v) for k, v in tuned_weights.items()}
    return out


def block_orderings(quick: bool) -> dict:
    """B: six canonical orderings on det_greedy + det_combo."""
    factories = make_factories()
    targets = ["greedy", "combo"]
    N = 300 if quick else 500
    out = {}
    for name in targets:
        c = build_corpus(name, factories[name], N, 0)
        out[name] = {}
        for oname, fn in ORDERINGS.items():
            t0 = time.time()
            res = measure_ordering(c, fn)
            res["wall_s"] = round(time.time() - t0, 2)
            out[name][oname] = res
            print(f"  ordering {name}/{oname}: H={res['H_T']:.3f} "
                  f"Hshan={res['H_shannon_bits']:.3f} S={res['S_T_bits']}", flush=True)
    return out


def _agent_ce_subsampled(agent, corpus: Corpus, n_positions: int, rng_seed: int) -> tuple[float, int]:
    """Subsampled cross-entropy. Scores agent at `n_positions` random positions drawn
    across the corpus, weighted so longer games contribute proportionally.
    Returns (H_bits_per_move, n_scored)."""
    # flatten (game_idx, move_idx) -> position list, then sample
    positions = [(gi, mi) for gi, g in enumerate(corpus.games)
                         for mi in range(len(g.moves))]
    if not positions:
        return 0.0, 0
    rng = random.Random(rng_seed)
    if len(positions) > n_positions:
        positions = rng.sample(positions, n_positions)
    # group by game so we can replay each only once
    by_game: dict[int, list[int]] = defaultdict(list)
    for gi, mi in positions:
        by_game[gi].append(mi)

    eps = 0.02
    bits = 0.0
    n = 0
    for gi, mi_list in by_game.items():
        mi_set = set(mi_list)
        game = corpus.games[gi]
        g = HexGame()
        for mi, mv in enumerate(game.moves):
            legal = g.legal_moves()
            if not legal or mv not in set(legal):
                break
            if mi in mi_set:
                random.seed(rng_seed * 7919 + gi * 31 + mi)
                dist_mv = agent.choose_move(g)
                p = (1 - eps) if mv == dist_mv else eps / max(1, len(legal) - 1)
                bits += -math.log2(max(p, 1e-12))
                n += 1
            g.make(*mv)
    return bits / max(1, n), n


def block_pareto(quick: bool) -> dict:
    """E.2: every agent's program length, plus cross-entropy on reference corpora.

    Subsamples positions (default 800) per (agent, ref) pair for tractable runtime."""
    factories = make_factories()
    N_corpus = 100 if quick else 300
    n_positions = 300 if quick else 800
    ref_names = ["random", "greedy", "combo"]
    refs = {n: build_corpus(n, factories[n], N_corpus, 0) for n in ref_names}

    out = {"agents": {}, "ref_corpora": ref_names, "N_corpus": N_corpus,
           "n_positions": n_positions}
    for aname, fac in factories.items():
        pl = program_length(AGENT_CLASS[aname])
        row = {"prog_bytes": pl, "H_T_on": {}, "n_scored_on": {}}
        for rname, corp in refs.items():
            t0 = time.time()
            try:
                h, n_sc = _agent_ce_subsampled(fac(), corp, n_positions, rng_seed=1234)
                row["H_T_on"][rname] = h
                row["n_scored_on"][rname] = n_sc
                print(f"  pareto {aname}->{rname}: H={h:.3f} n={n_sc} [{time.time()-t0:.1f}s]",
                      flush=True)
            except Exception as e:
                row["H_T_on"][rname] = None
                row[f"{rname}_error"] = repr(e)
                print(f"  pareto {aname}->{rname}: ERR {e!r}", flush=True)
        out["agents"][aname] = row
    return out


def block_matchups(quick: bool) -> dict:
    """E.4-ish: pairwise win rates + H_T of each agent as model on each matchup corpus."""
    factories = make_factories()
    names = list(factories.keys())
    games_per = 20 if quick else 40
    seeds = [0] if quick else [0, 1]

    winmat = {a: {b: {"wins": 0, "losses": 0, "draws": 0, "games": 0}
                   for b in names} for a in names}

    def play_one(a_fac, b_fac, seed):
        random.seed(seed)
        a, b = a_fac(), b_fac()
        g = HexGame()
        m = 0
        while g.winner is None and m < 150:
            ag = a if g.current_player == 1 else b
            legal = g.legal_moves()
            if not legal: break
            mv = ag.choose_move(g)
            if mv not in set(legal): mv = random.choice(legal)
            g.make(*mv); m += 1
        return g.winner  # 1, 2, or None

    print(f"  matchups: {len(names)}x{len(names)} roster, {games_per*len(seeds)} evidence/games/pair")
    for ai, a in enumerate(names):
        for bi, b in enumerate(names):
            if ai == bi: continue
            for seed in seeds:
                rng = random.Random(seed * 1000 + ai * 37 + bi)
                for i in range(games_per):
                    s = rng.randint(0, 2**31 - 1)
                    w = play_one(factories[a], factories[b], s)
                    winmat[a][b]["games"] += 1
                    if w == 1:   winmat[a][b]["wins"] += 1
                    elif w == 2: winmat[a][b]["losses"] += 1
                    else:        winmat[a][b]["draws"] += 1
        print(f"    {a} vs each: " + " ".join(
            f"{b}={winmat[a][b]['wins']}/{winmat[a][b]['games']}"
            for b in names if b != a), flush=True)

    # simple Bradley–Terry-ish winrate summary (symmetric: wins as p1 + losses as p2 inverse)
    summary = {}
    for a in names:
        total_w = sum(winmat[a][b]["wins"] for b in names if b != a)
        total_l = sum(winmat[a][b]["losses"] for b in names if b != a)
        total_d = sum(winmat[a][b]["draws"] for b in names if b != a)
        tot = total_w + total_l + total_d
        summary[a] = {
            "wins": total_w, "losses": total_l, "draws": total_d,
            "winrate": total_w / max(1, tot),
        }
    return {"matrix": winmat, "summary": summary, "games_per_pair": games_per * len(seeds)}


# ── driver ───────────────────────────────────────────────────────────────────

def main():
    ap = argparse.ArgumentParser()
    ap.add_argument("--quick", action="store_true",
                    help="Smaller Ns / fewer seeds for smoke testing")
    ap.add_argument("--skip", default="", help="Comma-separated block names to skip")
    ap.add_argument("--out", default="rigor_v1.json")
    args = ap.parse_args()

    skip = set(s.strip() for s in args.skip.split(",") if s.strip())

    t0 = time.time()
    out = {"meta": {
        "quick": args.quick,
        "timestamp": time.strftime("%Y-%m-%dT%H:%M:%S"),
    }}

    blocks = [
        ("scaling",   block_scaling),
        ("orderings", block_orderings),
        ("pareto",    block_pareto),
        ("matchups",  block_matchups),
    ]
    for name, fn in blocks:
        if name in skip:
            print(f"[skip] {name}", flush=True)
            continue
        print(f"\n=== {name} ===", flush=True)
        bt = time.time()
        out[name] = fn(args.quick)
        print(f"  done {name} in {time.time() - bt:.1f}s", flush=True)

    out["meta"]["total_wall_s"] = round(time.time() - t0, 1)
    path = RESULTS / args.out
    path.write_text(json.dumps(out, indent=2, default=str))
    print(f"\nwrote {path} [{out['meta']['total_wall_s']}s total]")


if __name__ == "__main__":
    main()
